/**
 * Look up city coordinates from built-in database
 */
export function lookupCity(cityName: string): LocationData | null {
  const normalized = cityName.trim();
  const cityData = CITY_DATABASE[normalized];

  if (cityData) {
    return {
      cityName: normalized,
      latitude: cityData.lat,
      longitude: cityData.lon,
      timezone: cityData.tz,
      utcOffset: 0 // Will be calculated dynamically
    };
  }

  return null;
}

/**
 * Calculate UTC offset for a given date and timezone
 */
export function getUTCOffset(date: Date, timezone: string): number {
  try {
    // Use Intl API to get the timezone offset
    const utcDate = new Date(date.toLocaleString('en-US', { timeZone: 'UTC' }));
    const tzDate = new Date(date.toLocaleString('en-US', { timeZone: timezone }));
    const offset = (tzDate.getTime() - utcDate.getTime()) / (1000 * 60 * 60);
    return offset;
  } catch (error) {
    console.warn(`Could not determine timezone offset for ${timezone}`, error);
    return 0;
  }
}

/**
 * Convert local time to UTC for a specific timezone
 */
export function localToUTC(localDate: Date, timezone: string): Date {
  try {
    // Get the UTC offset for this date/timezone
    const offset = getUTCOffset(localDate, timezone);

    // Create a new date adjusted to UTC
    const utcTime = new Date(localDate.getTime() - offset * 60 * 60 * 1000);
    return utcTime;
  } catch (error) {
    console.warn('Could not convert to UTC, using original date', error);
    return localDate;
  }
}

/**
 * Get timezone name for coordinates (approximate)
 */
export function getTimezoneFromCoordinates(latitude: number, longitude: number): string {
  // Simplified timezone estimation based on longitude
  // For more accurate results, you'd need a timezone database
  const hoursFromGMT = Math.round(longitude / 15);

  // Try to match with known timezones
  for (const [cityName, data] of Object.entries(CITY_DATABASE)) {
    const latDiff = Math.abs(data.lat - latitude);
    const lonDiff = Math.abs(data.lon - longitude);

    // If within ~1 degree, assume same timezone
    if (latDiff < 1 && lonDiff < 1) {
      return data.tz;
    }
  }

  // Fallback: return UTC offset string
  return `Etc/GMT${hoursFromGMT >= 0 ? '-' : '+'}${Math.abs(hoursFromGMT)}`;
}

